
/*
 * url 联系我们查询接口(H5)
 * 
*/ 
var contactUsInfoH5Service = {
    url : constant.INTERFACE_CONTACTUSLIST_H5,
    lock : false,
    request : function(data, option) {
        console.log("用户信息 service~~~~"+JSON.stringify(data));
        var self = this;
        if (self.lock) {
            return;
        }
        self.lock = true;
        $.ajax({
            url : self.url,
            type : 'GET',
            data : data,
            dataType : 'json',
            contentType: "application/json",
            success : function(data) {
                self.lock = false;
                data.status==0?option.success(data):option.error(data);//接口正常和异常处理
            },
            error : function(e, err) {
                self.lock = false
                option.error(err);
            }
        });
    }
};
_.extend(contactUsInfoH5Service, Backbone.Events);





